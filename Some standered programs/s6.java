//WAP to generate even or odd from 1 to 100.

public class s6 {
    public static void main(String [] args){
       
        //System.out.println("Enter ")
        for(int i=1; i<=100; i++){
            if(i%2==0){
                System.out.println(i+" is an even num");
            }
            else{
                System.out.println(i+" is an odd num");
            }
        }
    }
}
